    private void btnFinalizadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizadoActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea marcar el evento como finalizado?", "Confirmación", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            try {
                ObjetosNegocioFactory.crearEventosBO().modificarEstadoEvento(evento.getId(), evento.getEstado());
                JOptionPane.showMessageDialog(this, "Evento marcado como finalizado.");
                ControlFlujo.mostrarFrame(new FrmEventos());
                this.dispose();
            } catch (NegocioException e) {
                JOptionPane.showMessageDialog(this, "Error al marcar el evento como finalizado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnFinalizadoActionPerformed

    private void btnModificarEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActividades1ActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea modificar el evento?", "Confirmación", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            try {
                EventoDTO eventoModificado = new EventoDTO(
                        evento.getId(),
                        txtTitulo.getText(),
                        txtDescripcion.getText(),
                        Formateador.toCalendar(fechaInicio.getDate()),
                        Formateador.toCalendar(fechaFin.getDate()),
                        evento.getEstado(),
                        evento.getModalidad(),
                        null,
                        evento.getResponsableId()
                );
                ObjetosNegocioFactory.crearEventosBO().modificarEvento(eventoModificado);
                JOptionPane.showMessageDialog(this, "Evento modificado correctamente.");
                ControlFlujo.mostrarFrame(new FrmEventos());
                this.dispose();
            } catch (NegocioException e) {
                JOptionPane.showMessageDialog(this, "Error al modificar el evento: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnVerActividades1ActionPerformed



    private void cargarDatosEvento() {
        txtTitulo.setText(evento.getTitulo());
        txtDescripcion.setText(evento.getDescripcion());
        fechaInicio.setDate(toLocalDate(evento.getFechaInicio()));
        fechaFin.setDate(toLocalDate(evento.getFechaFin()));
        IResponsablesBO responsablesBO = ObjetosNegocioFactory.crearResponsablesBO();
        try {
            ResponsableDTO responsable = responsablesBO.obtenerResponsablePorId(evento.getResponsableId());
            lblOrganizador.setText(Formateador.formatearNombre(responsable.getNombre(), responsable.getApellidoPaterno(), responsable.getApellidoMaterno()));
        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar el responsable: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        comboBoxModalidad.removeAllItems();
        comboBoxModalidad.addItem(Formateador.formatearCap(evento.getModalidad().toString()));
        comboBoxEstado.removeAllItems();
        comboBoxEstado.addItem(Formateador.formatearCap(evento.getEstado().toString()));

        if (opcion == OPCION_VER) {
            txtTitulo.setEditable(false);
            txtDescripcion.setEditable(false);
            fechaInicio.setEnabled(false);
            fechaFin.setEnabled(false);
            comboBoxModalidad.setEditable(false);
            comboBoxEstado.setEditable(false);
            btnFinalizado.setVisible(false);
            btnModificarEvento.setVisible(false);
        } else {
            comboBoxModalidad.removeAll();
            ModalidadEvento[] modalidades = ModalidadEvento.values();
            for (ModalidadEvento modalidad : modalidades) {
                comboBoxModalidad.addItem(Formateador.formatearCap(modalidad.toString()));
            }
            comboBoxModalidad.setSelectedItem(Formateador.formatearCap(evento.getModalidad().toString()));
            comboBoxEstado.removeAll();
            EstadoEvento[] estados = EstadoEvento.values();
            for (EstadoEvento estado : estados) {
                comboBoxEstado.addItem(Formateador.formatearCap(estado.toString()));
            }
            comboBoxEstado.setSelectedItem(Formateador.formatearCap(evento.getEstado().toString()));
        }
    }

    private LocalDate toLocalDate(Calendar calendar) {
        return LocalDate.of(calendar.get(Calendar.YEAR), calendar.get(Calendar.MONTH) + 1,
                calendar.get(Calendar.DAY_OF_MONTH));
    }

